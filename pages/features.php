    </li>
  
    <li class="list-group-item">
      <h4>SEO friendly</h4>
      <p>Each content (page or view) can have its own personality (<strong>URL, title, canocical, next/previous page, description,....</strong>), and can be called separately.</p>
      <p>We could say that <strong>Ababool </strong>is a <strong>Multiple Single Web Page Application</strong>, because you can enter the application directly to each content (page), obtaining different entries (first pages) to the web application.</p>
