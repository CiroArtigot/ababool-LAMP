<h1>What</h1>

<h4>Ababool is a free and open source tool, developed with PHP5, HTML5, CSS3 and jQuery, for creating nice and functional single web page applications.</h4>
<ul class="list-group list-group-black">	
  <li class="list-group-item">
    <h4>Single Web Page  Application - AJAX</h4>
      <p><strong>Ababool</strong> is a <em><strong>&quot;Single Web Page Application&quot;</strong></em> so only loads one page. The page does not reload at any point in the process.</p>
      <p>
      All the new content is retrieved using <strong>AJAX</strong> calls, keeping the user all the time in the same page with no reloads.</p>
  </li>
  <li class="list-group-item">
    <h4>PHP5, HTML5, CSS3, XML, JSON, AJAX and jQuery</h4>
    <p><strong>Ababool</strong> is developed with <strong>PHP5</strong>, <strong>HTML5</strong>, <strong>CSS3</strong>, <strong>XML</strong>, <strong>JSON</strong>, <strong>AJAX</strong> and <strong>jQuery</strong>.</p>
  </li>
  
    <li class="list-group-item">
